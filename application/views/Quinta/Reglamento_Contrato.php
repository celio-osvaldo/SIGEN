
<p style="text-align: center; font-size: 14">
	<b>REGLAMENTO</b>
</p>

<p style="text-align: justify;">
Este Reglamento se considera anexo al Contrato y por lo tanto es de observancia para ambas partes:</p>
<p style="text-align: justify;">
1. Antes de dar inicio a su evento, El Arrendatario recorrerá́ las instalaciones solicitadas con el personal del Arrendador para revisar las condiciones generales en que se encuentra el Salón de eventos.</p>
<p style="text-align: justify;">
2. El salón para eventos sociales estará́ a su disposición sólo el tiempo estipulado en el Contrato.</p>
<p style="text-align: justify;">
3. Se otorgarán 30 (treinta) minutos de tolerancia para desocupar el salón al final del evento.</p>
<p style="text-align: justify;">
4. Está restringido fumar en áreas cerradas del salón (sanitarios y cocina).</p>
<p style="text-align: justify;">
5. En caso de que el Arrendatario requiera introducir al salón algún tipo de equipo adicional, deberá́ de contar con la autorización del Arrendador.</p>
<p style="text-align: justify;">
6. El Arrendatario que contrate personal para su evento, será́ responsable por la conducta de estos.</p>
<p style="text-align: justify;">
7. Está <b>prohibido clavar, engrapar o atornillar objetos en las paredes, techos, ventanas, piso o en cualquier otra parte de las instalaciones</b> del salón.</p>
<p style="text-align: justify;">
8. El Arrendador no se hace responsable por ningún daño o lesión que pudiesen sufrir sus invitados, salvo que sea por causas imputables al Arrendador.</p>
<p style="text-align: justify;">
9. No se permite la entrada de animales (mascotas), salvo casos justificados.</p>
<p style="text-align: justify;">
10.El cuidado de los niños es responsabilidad de los padres, tutores o responsables de los mismos.</p>
<p style="text-align: justify;">
11.El arrendador no se hace responsable del extravió u objetos olvidados del Arrendatario o sus invitados.</p>
<p style="text-align: justify;">
12.Queda estrictamente prohibido el acceso y el uso de cualquier tipo de arma.</p>
<p style="text-align: justify;">
13.El Arrendatario se responsabiliza de respetar y hacer respetar el presente Reglamento con sus invitados. En caso de daño, pagará el costo de su reparación o reposición estipulado en el Contrato.</p>
<p style="text-align: justify;">
14. <b>No se permite el uso de fuegos pirotécnicos al aire libre.</b></p>


